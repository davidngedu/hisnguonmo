        private void SaveProcess()
        {
            CommonParam param = new CommonParam();
            try
            {
                bool success = false;
                if (!btnEdit.Enabled && !btnAdd.Enabled)
                    return;

                positionHandle = -1;
                if (!dxValidationProviderEditorInfo.Validate())
                    return;

                HisStationSDO sdo = new HisStationSDO();
                WaitingManager.Show();
                MOS.EFMODEL.DataModels.HIS_STATION updateDTO = new MOS.EFMODEL.DataModels.HIS_STATION();
                HIS_ROOM room = new HIS_ROOM();

                if (this.currentData != null && this.currentData.ID > 0)
                {
                    LoadCurrent(this.currentData.ID, ref updateDTO);
                }
                UpdateDTOFromDataForm(ref updateDTO);
                sdo.HisStation = updateDTO;

                if (cboDepartment.EditValue != null) room.DEPARTMENT_ID = Inventec.Common.TypeConvert.Parse.ToInt64((cboDepartment.EditValue ?? "0").ToString());
                if (chkPause.EditValue != null) room.IS_PAUSE = (short)(chkPause.Checked ? 1 : 0);
                room.ROOM_TYPE_ID = IMSys.DbConfig.HIS_RS.HIS_ROOM_TYPE.ID__TR;
                sdo.HisRoom = room;

                if (ActionType == GlobalVariables.ActionAdd)
                {
                    updateDTO.IS_ACTIVE = 1;
                    var resultData = new BackendAdapter(param).Post<MOS.EFMODEL.DataModels.HIS_STATION>(HisRequestUriStore.MOSHIS_STATION_CREATE, ApiConsumers.MosConsumer, sdo, param);
                    if (resultData != null)
                    {
                        success = true;
                        FillDataToGridControl();
                        SetDefaultValue();
                    }
                }
                else
                {
                    if (currentData != null)
                    {
                        sdo.HisRoom.ID = currentData.ROOM_ID;
                    }
                    var resultData = new BackendAdapter(param).Post<MOS.EFMODEL.DataModels.HIS_STATION>(HisRequestUriStore.MOSHIS_STATION_UPDATE, ApiConsumers.MosConsumer, sdo, param);
                    if (resultData != null)
                    {
                        success = true;
                        //UpdateRowDataAfterEdit(resultData);
                        FillDataToGridControl();
                    }
                }

                if (success)
                {
                    SetFocusEditor();
                }

                WaitingManager.Hide();

                #region Hien thi message thong bao
                MessageManager.Show(this, param, success);
                #endregion

                #region Neu phien lam viec bi mat, phan mem tu dong logout va tro ve trang login
                SessionManager.ProcessTokenLost(param);
                #endregion
            }
            catch (Exception ex)
            {
                WaitingManager.Hide();
                Inventec.Common.Logging.LogSystem.Warn(ex);
            }
        }

