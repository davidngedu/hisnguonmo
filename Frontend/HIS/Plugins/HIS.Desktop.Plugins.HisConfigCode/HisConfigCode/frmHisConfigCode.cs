        private void SaveProcess()
        {
            CommonParam param = new CommonParam();
            try
            {
                bool success = false;
                if (!btnEdit.Enabled && !btnAdd.Enabled)
                    return;

                positionHandle = -1;
                if (!dxValidationProviderEditorInfo.Validate())
                    return;

                WaitingManager.Show();
                MOS.EFMODEL.DataModels.HIS_CONFIG updateDTO = new MOS.EFMODEL.DataModels.HIS_CONFIG();

                if (this.currentData != null && this.currentData.ID > 0)
                {
                    if (this.ActionType == GlobalVariables.ActionEdit)
                        LoadCurrent(this.currentData.ID, ref updateDTO);
                }
                UpdateDTOFromDataForm(ref updateDTO);
                if (ActionType == GlobalVariables.ActionAdd)
                {
                    updateDTO.IS_ACTIVE = IMSys.DbConfig.HIS_RS.COMMON.IS_ACTIVE__TRUE;
                    if (this.CheckKey(updateDTO) == true)
                    {
                        var resultData = new BackendAdapter(param).Post<MOS.EFMODEL.DataModels.HIS_CONFIG>(HisRequestUriStore.MOSHIS_CONFIG_CREATE, ApiConsumers.MosConsumer, updateDTO, param);
                        if (resultData != null)
                        {
                            success = true;
                            FillDataToGridControl();
                            ResetFormData();
                            if (chkRefreshConfig.Checked)
                                HIS.Desktop.LocalStorage.HisConfig.ConfigLoader.Refresh();
                        }
                    }
                    else
                    {
                        param.Messages.Add("Key đã tồn tại");
                    }
                }
                else
                {
                    var resultData = new BackendAdapter(param).Post<MOS.EFMODEL.DataModels.HIS_CONFIG>(HisRequestUriStore.MOSHIS_CONFIG_UPDATE, ApiConsumers.MosConsumer, updateDTO, param);
                    if (resultData != null)
                    {
                        success = true;
                        FillDataToGridControl();
                        if (chkRefreshConfig.Checked)
                            HIS.Desktop.LocalStorage.HisConfig.ConfigLoader.Refresh();
                    }
                }

                if (success)
                {
                    SetFocusEditor();
                }

                WaitingManager.Hide();

                #region Hien thi message thong bao
                MessageManager.Show(this, param, success);
                #endregion

                #region Neu phien lam viec bi mat, phan mem tu dong logout va tro ve trang login
                SessionManager.ProcessTokenLost(param);
                #endregion
            }
            catch (Exception ex)
            {
                WaitingManager.Hide();
                Inventec.Common.Logging.LogSystem.Warn(ex);
            }
        }

        private bool CheckKey (HIS_CONFIG updateDTO)
        {
            bool result = true;
            try
            {
                CommonParam param = new CommonParam();
                HisConfigFilter filter = new HisConfigFilter();
                    filter.KEY_WORD = updateDTO.KEY;
                    List<HIS_CONFIG> data = new BackendAdapter(param).Get<List<HIS_CONFIG>>(HisRequestUriStore.MOSHIS_CONFIG_GET, ApiConsumers.MosConsumer, filter, param);
                    if (data != null)
                    {
                        if (data.Exists(o => o.KEY == updateDTO.KEY && o.BRANCH_ID == updateDTO.BRANCH_ID))
                        {
                            result = false;
                        }
                    }
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Warn(ex);
                result = false;
            }
            return result;
        }

