        private void SaveProcess()
        {
            CommonParam param = new CommonParam();
            try
            {
                bool success = false;
                if (!btnEdit.Enabled && !btnAdd.Enabled)
                    return;

                positionHandle = -1;
                if (!dxValidationProviderEditorInfo.Validate())
                    return;

                WaitingManager.Show();
                MOS.EFMODEL.DataModels.HIS_MEDICINE_USE_FORM updateDTO = new MOS.EFMODEL.DataModels.HIS_MEDICINE_USE_FORM();

                if (this.currentData != null && this.currentData.ID > 0)
                {
                    LoadCurrent(this.currentData.ID, ref updateDTO);
                }
                UpdateDTOFromDataForm(ref updateDTO);
                if (ActionType == GlobalVariables.ActionAdd)
                {
                    resultData = new BackendAdapter(param).
                      Post<MOS.EFMODEL.DataModels.HIS_MEDICINE_USE_FORM>
                      (HisRequestUriStore.MOSHIS_MEDICINE_USE_FORM_CREATE,
                      ApiConsumers.MosConsumer, updateDTO, param);
                    if (resultData != null)
                    {
                        success = true;
                        FillDataToGridControl();
                        ResetFormData();
                        RefeshDataAfterSave();
                        BackendDataWorker.Reset<MOS.EFMODEL.DataModels.HIS_MEDICINE_USE_FORM>();
                    }
                }
                else
                {
                    resultData = new BackendAdapter(param).Post<MOS.EFMODEL.DataModels.HIS_MEDICINE_USE_FORM>
                      (HisRequestUriStore.MOSHIS_MEDICINE_USE_FORM_UPDATE, ApiConsumers.MosConsumer, updateDTO, param);
                    if (resultData != null)
                    {
                        success = true;
                        UpdateRowDataAfterEdit(resultData);
                        RefeshDataAfterSave();
                        BackendDataWorker.Reset<MOS.EFMODEL.DataModels.HIS_MEDICINE_USE_FORM>();
                    }
                }

                if (success)
                {
                    SetFocusEditor();
                }

                WaitingManager.Hide();

                #region Hien thi message thong bao
                MessageManager.Show(this, param, success);
                #endregion

                #region Neu phien lam viec bi mat, phan mem tu dong logout va tro ve trang login
                SessionManager.ProcessTokenLost(param);
                #endregion
            }
            catch (Exception ex)
            {
                WaitingManager.Hide();
                Inventec.Common.Logging.LogSystem.Warn(ex);
            }
        }

