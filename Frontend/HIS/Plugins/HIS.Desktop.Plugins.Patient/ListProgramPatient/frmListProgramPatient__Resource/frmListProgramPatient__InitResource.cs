                this.gridColumn5.Caption = Inventec.Common.Resource.Get.Value("frmListProgramPatient.gridColumn5.Caption", Resources.ResourceLanguageManager.LanguageResource, LanguageManager.GetCulture());
                this.gridColumn6.Caption = Inventec.Common.Resource.Get.Value("frmListProgramPatient.gridColumn6.Caption", Resources.ResourceLanguageManager.LanguageResource, LanguageManager.GetCulture());
                this.Text = Inventec.Common.Resource.Get.Value("frmListProgramPatient.Text", Resources.ResourceLanguageManager.LanguageResource, LanguageManager.GetCulture());
