        private void chkNotRequiredUseForm_KeyUp(object sender, KeyEventArgs e)
        {
            try
            {
                if (e.KeyCode == Keys.Space)
                {
                    chkNotRequiredUseForm.Checked = !chkNotRequiredUseForm.Checked;
                }
                if (e.KeyCode == Keys.Enter)
                {
                    if (this.ActionType == GlobalVariables.ActionEdit && btnEdit.Enabled)
                    {
                        btnEdit.Focus();
                    }
                }
                e.Handled = true;
            }
