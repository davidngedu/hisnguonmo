            bool valid = true;
            try
            {
                if (listId != null && listId.Count > 0)
                {
                    AcsAuthenRequestFilterQuery filter = new AcsAuthenRequestFilterQuery();
                    filter.IDs = listId;
                    List<ACS_AUTHEN_REQUEST> listData = new AcsAuthenRequestGet().Get(filter);
                    if (listData != null && listData.Count > 0)
                    {
                        foreach (var data in listData)
                        {
                            if (IMSys.DbConfig.ACS_RS.COMMON.IS_ACTIVE__TRUE != data.IS_ACTIVE)
                            {
                                valid = false;
                                break;
                            }
                        }
                        if (!valid)
                        {
                            MessageUtil.SetMessage(param, LibraryMessage.Message.Enum.Common__DuLieuDangBiKhoa);
                        }
                    }
                }
            }
            catch (Exception ex)
            {
                LogSystem.Error(ex);
                valid = false;
                param.HasException = true;
            }
            return valid;
        }

