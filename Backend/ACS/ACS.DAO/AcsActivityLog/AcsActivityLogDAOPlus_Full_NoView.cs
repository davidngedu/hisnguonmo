using ACS.DAO.StagingObject;
using ACS.EFMODEL.DataModels;
using Inventec.Core;
using System;
using System.Collections.Generic;

namespace ACS.DAO.AcsActivityLog
{
    public partial class AcsActivityLogDAO : EntityBase
    {
        public ACS_ACTIVITY_LOG GetByCode(string code, AcsActivityLogSO search)
        {
            ACS_ACTIVITY_LOG result = null;

            try
            {
                result = GetWorker.GetByCode(code, search);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = null;
            }

            return result;
        }

        public Dictionary<string, ACS_ACTIVITY_LOG> GetDicByCode(AcsActivityLogSO search, CommonParam param)
        {
            Dictionary<string, ACS_ACTIVITY_LOG> result = new Dictionary<string, ACS_ACTIVITY_LOG>();
            try
            {
                result = GetWorker.GetDicByCode(search, param);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result.Clear();
            }

            return result;
        }

        public bool ExistsCode(string code, long? id)
        {

            try
            {
                return CheckWorker.ExistsCode(code, id);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                throw;
            }
        }
    }
}
