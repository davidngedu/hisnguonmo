using ACS.DAO.StagingObject;
using ACS.EFMODEL.DataModels;
using Inventec.Core;
using System;
using System.Collections.Generic;

namespace ACS.DAO.AcsOtpType
{
    public partial class AcsOtpTypeDAO : EntityBase
    {
        public ACS_OTP_TYPE GetByCode(string code, AcsOtpTypeSO search)
        {
            ACS_OTP_TYPE result = null;

            try
            {
                result = GetWorker.GetByCode(code, search);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = null;
            }

            return result;
        }

        public Dictionary<string, ACS_OTP_TYPE> GetDicByCode(AcsOtpTypeSO search, CommonParam param)
        {
            Dictionary<string, ACS_OTP_TYPE> result = new Dictionary<string, ACS_OTP_TYPE>();
            try
            {
                result = GetWorker.GetDicByCode(search, param);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result.Clear();
            }

            return result;
        }

        public bool ExistsCode(string code, long? id)
        {

            try
            {
                return CheckWorker.ExistsCode(code, id);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                throw;
            }
        }
    }
}
