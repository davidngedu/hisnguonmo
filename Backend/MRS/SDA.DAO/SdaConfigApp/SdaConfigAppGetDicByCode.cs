using SDA.DAO.Base;
using SDA.DAO.StagingObject;
using SDA.EFMODEL.DataModels;
using Inventec.Common.Logging;
using Inventec.Core;
using System;
using System.Collections.Generic;
using System.Linq;

namespace SDA.DAO.SdaConfigApp
{
    partial class SdaConfigAppGet : EntityBase
    {
        public Dictionary<string, SDA_CONFIG_APP> GetDicByCode(SdaConfigAppSO search, CommonParam param)
        {
            Dictionary<string, SDA_CONFIG_APP> dic = new Dictionary<string, SDA_CONFIG_APP>();
            try
            {
                List<SDA_CONFIG_APP> listRecord = Get(search, param);
                if (listRecord != null)
                {
                    foreach (var item in listRecord)
                    {
                        if (!dic.ContainsKey(item.KEY))
                        {
                            dic.Add(item.KEY, item);
                        }
                    }
                }
            }
            catch (Exception ex)
            {
                Logging(Inventec.Common.Logging.LogUtil.TraceData(Inventec.Common.Logging.LogUtil.GetMemberName(() => search), search) + Inventec.Common.Logging.LogUtil.TraceData(Inventec.Common.Logging.LogUtil.GetMemberName(() => param), param), LogType.Error);
                LogSystem.Error(ex);
                dic.Clear();
            }
            return dic;
        }
    }
}
