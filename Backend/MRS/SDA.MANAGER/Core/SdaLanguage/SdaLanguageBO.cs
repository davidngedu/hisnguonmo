using System;

namespace SDA.MANAGER.Core.SdaLanguage
{
    partial class SdaLanguageBO : BusinessObjectBase
    {
        internal SdaLanguageBO()
            : base()
        {

        }

        internal T Get<T>(object data)
        {
            T result = default(T);
            try
            {
                IDelegacyT delegacy = new SdaLanguageGet(param, data);
                result = delegacy.Execute<T>();
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = default(T);
            }
            return result;
        }
    }
}
