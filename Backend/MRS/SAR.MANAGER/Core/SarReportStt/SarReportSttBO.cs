using System;

namespace SAR.MANAGER.Core.SarReportStt
{
    partial class SarReportSttBO : BusinessObjectBase
    {
        internal SarReportSttBO()
            : base()
        {

        }

        internal T Get<T>(object data)
        {
            T result = default(T);
            try
            {
                IDelegacyT delegacy = new SarReportSttGet(param, data);
                result = delegacy.Execute<T>();
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = default(T);
            }
            return result;
        }
    }
}
