        public List<SAR_FORM_TYPE> Get(SarFormTypeSO search, CommonParam param)
        {
            List<SAR_FORM_TYPE> result = new List<SAR_FORM_TYPE>();
            try
            {
                result = GetWorker.Get(search, param);
            }
            catch (Exception ex)
            {
                param.HasException = true;
                Inventec.Common.Logging.LogSystem.Error(ex);
                result.Clear();
            }
            return result;
        }

        public SAR_FORM_TYPE GetById(long id, SarFormTypeSO search)
        {
            SAR_FORM_TYPE result = null;
            try
            {
