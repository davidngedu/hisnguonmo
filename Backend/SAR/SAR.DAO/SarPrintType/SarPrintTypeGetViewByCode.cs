using SAR.DAO.Base;
using SAR.DAO.StagingObject;
using SAR.EFMODEL.DataModels;
using Inventec.Common.Logging;
using Inventec.Core;
using System;
using System.Collections.Generic;
using System.Linq;

namespace SAR.DAO.SarPrintType
{
    partial class SarPrintTypeGet : EntityBase
    {
        public V_SAR_PRINT_TYPE GetViewByCode(string code, SarPrintTypeSO search)
        {
            V_SAR_PRINT_TYPE result = null;
            try
            {
                bool valid = true;
                valid = valid && IsNotNullOrEmpty(code);
                if (valid)
                {
                    using (var ctx = new AppContext())
                    {
                        var query = ctx.V_SAR_PRINT_TYPE.AsQueryable().Where(p => p.PRINT_TYPE_CODE == code);
                        if (search.listVSarPrintTypeExpression != null && search.listVSarPrintTypeExpression.Count > 0)
                        {
                            foreach (var item in search.listVSarPrintTypeExpression)
                            {
                                query = query.Where(item);
                            }
                        }
                        result = query.SingleOrDefault();
                    }
                }
            }
            catch (Exception ex)
            {
                Logging(Inventec.Common.Logging.LogUtil.TraceData(Inventec.Common.Logging.LogUtil.GetMemberName(() => code), code) + Inventec.Common.Logging.LogUtil.TraceData(Inventec.Common.Logging.LogUtil.GetMemberName(() => search), search), LogType.Error);
                LogSystem.Error(ex);
                result = null;
            }
            return result;
        }
    }
}
