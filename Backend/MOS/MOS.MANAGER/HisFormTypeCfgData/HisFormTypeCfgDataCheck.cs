        internal bool CheckExists(long fromTypeCfgId, string formTypeCode, long? id)
        {
            bool valid = true;
            try
            {
                HisFormTypeCfgDataFilterQuery filter = new HisFormTypeCfgDataFilterQuery();
                filter.FORM_TYPE_CFG_ID = fromTypeCfgId;
                filter.FORM_TYPE_CODE__EXACT = formTypeCode;
                filter.ID__NOT_EQUAL = id;
                List<HIS_FORM_TYPE_CFG_DATA> exists = new HisFormTypeCfgDataGet().Get(filter);
                if (IsNotNullOrEmpty(exists))
                {
                    HIS_FORM_TYPE_CFG ftcfg = new HisFormTypeCfgGet().GetById(fromTypeCfgId);
                    string cfgName = ftcfg != null ? ftcfg.DESCRIPTION : "";
                    MANAGER.Base.MessageUtil.SetMessage(param, LibraryMessage.Message.Enum.HisFormTypeCfgData_FormMaDaDuocCauHinh, formTypeCode, cfgName);
                    return false;
                }
            }
            catch (Exception ex)
            {
                LogSystem.Error(ex);
                valid = false;
                param.HasException = true;
            }
            return valid;
        }
