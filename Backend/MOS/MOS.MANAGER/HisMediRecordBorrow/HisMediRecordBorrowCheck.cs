        internal bool VerifyRequireField(HIS_MEDI_RECORD_BORROW data)
        {
            bool valid = true;
            try
            {
                if (data == null) throw new ArgumentNullException("data");
                if (data.MEDI_RECORD_ID <= 0) throw new ArgumentNullException("data.MEDI_RECORD_ID");
                if (data.DEPARTMENT_ID <= 0) throw new ArgumentNullException("data.DEPARTMENT_ID");
            }
            catch (ArgumentNullException ex)
            {
                BugUtil.SetBugCode(param, LibraryBug.Bug.Enum.ThieuThongTinBatBuoc);
                LogSystem.Warn(ex);
                valid = false;
            }
            catch (Exception ex)
            {
                LogSystem.Error(ex);
                valid = false;
                param.HasException = true;
            }
            return valid;
        }

