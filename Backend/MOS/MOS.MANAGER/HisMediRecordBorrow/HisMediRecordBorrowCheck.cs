        internal bool IsNotBorrowed(List<long> mediRecordIds)
        {
            bool valid = true;
            try
            {
                if (IsNotNullOrEmpty(mediRecordIds))
                {
                    HisMediRecordBorrowViewFilterQuery filter = new HisMediRecordBorrowViewFilterQuery();
                    filter.IS_RECEIVE = false;
                    filter.MEDI_RECORD_IDs = mediRecordIds;
                    List<V_HIS_MEDI_RECORD_BORROW> notReceives = new HisMediRecordBorrowGet().GetView(filter);
                    if (IsNotNullOrEmpty(notReceives))
                    {
                        List<string> storeCodes = notReceives.Select(o => o.STORE_CODE).ToList();
                        string storeCodeStr = string.Join(",", storeCodes);
                        MANAGER.Base.MessageUtil.SetMessage(param, LibraryMessage.Message.Enum.HisTreatmentBorrow_HoSoBenhAnDangDuocChoMuon, storeCodeStr);
                        return false;
                    }
                }
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                valid = false;
            }
            return valid;
        }

