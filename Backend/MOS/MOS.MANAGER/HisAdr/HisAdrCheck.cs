        internal bool VerifyRequireField(HIS_ADR data)
        {
            bool valid = true;
            try
            {
                if (data == null) throw new ArgumentNullException("data");
                if (data.TREATMENT_ID <= 0) throw new ArgumentNullException("data.TREATMENT_ID");
                if (data.ADR_TIME <= 0) throw new ArgumentNullException("data.ADR_TIME");
                if (String.IsNullOrEmpty(data.REPORT_LOGINNAME)) throw new ArgumentNullException("data.REPORT_LOGINNAME");
                if (data.ADR_RESULT_ID.HasValue)
                {
                    if (data.ADR_RESULT_ID.Value < 1 || data.ADR_RESULT_ID.Value > 7) throw new ArgumentNullException("data.ADR_RESULT_ID");
                }
                if (data.RELATIONSHIP_ID.HasValue)
                {
                    if (data.RELATIONSHIP_ID.Value < 1 || data.RELATIONSHIP_ID.Value > 7) throw new ArgumentNullException("data.RELATIONSHIP_ID");
                    if (data.RELATIONSHIP_ID.Value == IMSys.DbConfig.HIS_RS.HIS_ADR.RELATIONSHIP_ID__Khac
                        && String.IsNullOrWhiteSpace(data.RELATIONSHIP_ORTHER))
                    {
                        throw new ArgumentNullException("data.RELATIONSHIP_ORTHER");
                    }
                }
                if (data.SERIOUS_LEVEL.HasValue)
                {
                    if (data.SERIOUS_LEVEL.Value < 1 || data.SERIOUS_LEVEL.Value > 6) throw new ArgumentNullException("data.SERIOUS_LEVEL");
                }
                if (data.EXPERTISE_STANDER_ID.HasValue)
                {
                    if (data.EXPERTISE_STANDER_ID.Value < 1 || data.EXPERTISE_STANDER_ID.Value > 3) throw new ArgumentNullException("data.EXPERTISE_STANDER_ID");
                    if (data.EXPERTISE_STANDER_ID.Value == IMSys.DbConfig.HIS_RS.HIS_ADR.EXPERTISE_STANDER_ID__OTHER
                        && String.IsNullOrWhiteSpace(data.EXPERTISE_STANDER_OTHER))
                    {
                        throw new ArgumentNullException("data.EXPERTISE_STANDER_OTHER");
                    }
                }

            }
            catch (ArgumentNullException ex)
            {
                BugUtil.SetBugCode(param, LibraryBug.Bug.Enum.ThieuThongTinBatBuoc);
                LogSystem.Warn(ex);
                valid = false;
            }
            catch (Exception ex)
            {
                LogSystem.Error(ex);
                valid = false;
                param.HasException = true;
            }
            return valid;
        }

