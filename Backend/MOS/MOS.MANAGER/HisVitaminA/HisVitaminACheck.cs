        public static bool IsChild(long dob)
        {
            DateTime? dateOfBirth = Inventec.Common.DateTime.Convert.TimeNumberToSystemDateTime(dob);
            DateTime today = DateTime.Today;
            int month = GetMonthsBetween(dateOfBirth.Value, today);
            if (month > 60)
            {
                return false;
            }
            return true;
        }

        private static int GetMonthsBetween(DateTime from, DateTime to)
        {
            if (from > to) return GetMonthsBetween(to, from);

            var monthDiff = Math.Abs((to.Year * 12 + (to.Month - 1)) - (from.Year * 12 + (from.Month - 1)));

            if (from.AddMonths(monthDiff) > to || to.Day < from.Day)
            {
                return monthDiff - 1;
            }
            else
            {
                return monthDiff;
            }
        }

