        internal bool IsNotExist(HIS_SURG_REMUNERATION data)
        {
            bool valid = true;
            try
            {
                HisSurgRemunerationFilterQuery filter = new HisSurgRemunerationFilterQuery();
                filter.EMOTIONLESS_METHOD_ID = data.EMOTIONLESS_METHOD_ID;
                filter.PTTT_GROUP_ID = data.PTTT_GROUP_ID;
                filter.SERVICE_TYPE_ID = data.SERVICE_TYPE_ID;
                filter.ID_NOT_EQUAL = data.ID;

                List<HIS_SURG_REMUNERATION> rs = new HisSurgRemunerationGet().Get(filter);
                if (IsNotNullOrEmpty(rs))
                {
                    HIS_PTTT_GROUP ptttGroup = new HisPtttGroupGet().GetById(data.PTTT_GROUP_ID);
                    HIS_EMOTIONLESS_METHOD emotionlessMethod = data.EMOTIONLESS_METHOD_ID.HasValue ? new HisEmotionlessMethodGet().GetById(data.EMOTIONLESS_METHOD_ID.Value) : null;
                    HIS_SERVICE_TYPE serviceType = HisServiceTypeCFG.DATA.Where(o => o.ID == data.SERVICE_TYPE_ID).FirstOrDefault();
                    string emotionlessMethodName = emotionlessMethod != null ? emotionlessMethod.EMOTIONLESS_METHOD_NAME : "";
                    MOS.MANAGER.Base.MessageUtil.SetMessage(param, MOS.LibraryMessage.Message.Enum.HisSurgRemuneration_TonTaiDuLieu, serviceType.SERVICE_TYPE_NAME, ptttGroup.PTTT_GROUP_NAME, emotionlessMethodName);
                    return false;
                }
            }
            catch (Exception ex)
            {
                LogSystem.Error(ex);
                valid = false;
                param.HasException = true;
            }
            return valid;
        }
