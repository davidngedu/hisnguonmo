        internal bool IsNotExsists(HIS_SERVICE_NUM_ORDER data)
        {
            bool valid = true;
            try
            {
                HisServiceNumOrderFilterQuery filter = new HisServiceNumOrderFilterQuery();
                filter.SERVICE_ID = data.SERVICE_ID;
                filter.REQUEST_ROOM_ID = data.REQUEST_ROOM_ID;
                filter.ID__NOT_EQUAL = data.ID;
                List<HIS_SERVICE_NUM_ORDER> exists = new HisServiceNumOrderGet().Get(filter);
                if (IsNotNullOrEmpty(exists))
                {
                    V_HIS_SERVICE service = HisServiceCFG.DATA_VIEW.FirstOrDefault(o => o.ID == data.SERVICE_ID);
                    V_HIS_ROOM room = HisRoomCFG.DATA.FirstOrDefault(o => o.ID == data.REQUEST_ROOM_ID);
                    string serviceName = service != null ? service.SERVICE_NAME : "";
                    string roomName = room != null ? room.ROOM_NAME : "";

                    MANAGER.Base.MessageUtil.SetMessage(param, LibraryMessage.Message.Enum.HisServiceNumOrder_TonTaiDuLieu, serviceName, roomName);
                    return false;
                }
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                valid = false;
                param.HasException = true;
            }
            return valid;
        }
