        internal bool VerifyRequireField(HIS_SERVICE_NUM_ORDER data)
        {
            bool valid = true;
            try
            {
                if (data == null) throw new ArgumentNullException("data");
                if (data.SERVICE_ID <= 0) throw new ArgumentNullException("data.SERVICE_ID");
                if (data.REQUEST_ROOM_ID <= 0) throw new ArgumentNullException("data.REQUEST_ROOM_ID");
