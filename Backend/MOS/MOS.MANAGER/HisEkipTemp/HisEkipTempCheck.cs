        internal bool IsNotDuplicate(HIS_EKIP_TEMP data)
        {
            bool result = true;
            try
            {
                List<HIS_EKIP_TEMP_USER> ekipUsers = data != null ? data.HIS_EKIP_TEMP_USER.ToList() : null;

                if (IsNotNullOrEmpty(ekipUsers))
                {
                    bool check = ekipUsers
                        .GroupBy(o => new { o.LOGINNAME, o.EXECUTE_ROLE_ID })
                        .Where(grp => grp.Count() > 1).Any();

                    if (check)
                    {
                        MOS.MANAGER.Base.MessageUtil.SetMessage(param, MOS.LibraryMessage.Message.Enum.HisEkipTemp_TonTaiDuLieuTrungNhau);
                        return false;
                    }
                }
            }
            catch (Exception ex)
            {
                LogSystem.Error(ex);
                result = false;
            }
            return result;
        }
