        internal bool VerifyRequireField(HIS_EKIP_TEMP data)
        {
            bool valid = true;
            try
            {
                if (data == null) throw new ArgumentNullException("data");
                if (data.HIS_EKIP_TEMP_USER == null || data.HIS_EKIP_TEMP_USER.Count <= 0) throw new ArgumentNullException("data.HIS_EKIP_TEMP_USER");
                if (string.IsNullOrWhiteSpace(data.EKIP_TEMP_NAME)) throw new ArgumentNullException("data.EKIP_TEMP_NAME");
            }
            catch (ArgumentNullException ex)
            {
                BugUtil.SetBugCode(param, LibraryBug.Bug.Enum.ThieuThongTinBatBuoc);
                LogSystem.Warn(ex);
                valid = false;
            }
            catch (Exception ex)
            {
                LogSystem.Error(ex);
                valid = false;
                param.HasException = true;
            }
            return valid;
        }

