        internal bool VerifyRequireField(HIS_SESE_DEPO_REPAY data)
        {
            bool valid = true;
            try
            {
                if (data == null) throw new ArgumentNullException("data");
                if (!IsGreaterThanZero(data.SERE_SERV_DEPOSIT_ID)) throw new ArgumentNullException("data.SERE_SERV_DEPOSIT_ID");
                if (!IsGreaterThanZero(data.REPAY_ID)) throw new ArgumentNullException("data.REPAY_ID");
                if (!IsGreaterThanZero(data.TDL_EXECUTE_DEPARTMENT_ID)) throw new ArgumentNullException("data.TDL_EXECUTE_DEPARTMENT_ID");
                if (!IsGreaterThanZero(data.TDL_PATIENT_TYPE_ID)) throw new ArgumentNullException("data.TDL_EXECUTE_DEPARTMENT_ID");
                if (!IsGreaterThanZero(data.TDL_REQUEST_DEPARTMENT_ID)) throw new ArgumentNullException("data.TDL_EXECUTE_DEPARTMENT_ID");
                if (!IsGreaterThanZero(data.TDL_SERVICE_ID)) throw new ArgumentNullException("data.TDL_SERVICE_ID");
                if (data.TDL_SERVICE_REQ_ID == null) throw new ArgumentNullException("data.TDL_SERVICE_REQ_ID");
                if (!IsGreaterThanZero(data.TDL_SERVICE_TYPE_ID)) throw new ArgumentNullException("data.TDL_SERVICE_TYPE_ID");
                if (!IsGreaterThanZero(data.TDL_SERVICE_UNIT_ID)) throw new ArgumentNullException("data.TDL_SERVICE_UNIT_ID");
                if (!IsGreaterThanZero(data.TDL_TREATMENT_ID)) throw new ArgumentNullException("data.TDL_TREATMENT_ID");
            }
            catch (ArgumentNullException ex)
            {
                BugUtil.SetBugCode(param, LibraryBug.Bug.Enum.ThieuThongTinBatBuoc);
                LogSystem.Warn(ex);
                valid = false;
            }
            catch (Exception ex)
            {
                LogSystem.Error(ex);
                valid = false;
                param.HasException = true;
            }
            return valid;
        }

