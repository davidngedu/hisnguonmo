        internal bool VerifyDuplicate(HIS_CARER_CARD data)
        {
            bool valid = true;
            try
            {
                HisCarerCardFilterQuery filter = new HisCarerCardFilterQuery();
                filter.CARER_CARD_NUMBER__EXACT = data.CARER_CARD_NUMBER;
                List<HIS_CARER_CARD> listData = new HisCarerCardGet().Get(filter);
                if (IsNotNullOrEmpty(listData) && listData.Exists(o => o.ID != data.ID))
                {
                    MessageUtil.SetMessage(param, LibraryMessage.Message.Enum.DuLieuDaTonTaiTrenHeThong);
                    Logging("ListId invalid." + Inventec.Common.Logging.LogUtil.TraceData(Inventec.Common.Logging.LogUtil.GetMemberName(() => listData), listData), LogType.Error);
