        /// <summary>
        /// Kiem tra ma da ton tai hay chua, id duoc su dung trong truong hop muon bo qua chinh ma cua minh
        /// </summary>
        /// <param name="id"></param>
        /// <param name="data"></param>
        /// <returns></returns>
        internal bool ExistsCode(string code, long? id)
        {
            bool valid = true;
            try
            {
                List<HIS_SOURCE_MEDICINE> listAlls = new HisSourceMedicineGet().Get(new HisSourceMedicineFilterQuery());
                if (IsNotNullOrEmpty(listAlls))
                {
                    List<HIS_SOURCE_MEDICINE> sourceMedicines = listAlls.Where(o => o.SOURCE_MEDICINE_CODE == code && o.ID != id && (o.IS_DELETE.HasValue || o.IS_DELETE.Value != (short)1)).ToList();
                    if (IsNotNullOrEmpty(sourceMedicines))
                    {
                        string ten = string.Join(",", sourceMedicines.Select(o => o.SOURCE_MEDICINE_NAME).ToList());
                        MessageUtil.SetMessage(param, LibraryMessage.Message.Enum.HisSourceMedicine_DaTonTaiMaDaDuocGanVoiTen, code, ten);
                        valid = false;
                    }
                }
            }
            catch (Exception ex)
            {
                LogSystem.Error(ex);
                valid = false;
                param.HasException = true;
            }
            return valid;
        }

