        internal bool IsNotDuplicate(HIS_EXME_REASON_CFG data, HIS_EXME_REASON_CFG raw)
        {
            bool valid = true;
            try
            {
                HisExmeReasonCfgViewFilterQuery filter = new HisExmeReasonCfgViewFilterQuery();
                filter.PATIENT_CLASSIFY_ID = data.PATIENT_CLASSIFY_ID;
                filter.TREATMENT_TYPE_ID = data.TREATMENT_TYPE_ID;
                filter.PATIENT_TYPE_ID = data.PATIENT_TYPE_ID;
                filter.ID__NOT_EQUAL = data.ID;

                List<V_HIS_EXME_REASON_CFG> reasonCFGs = new HisExmeReasonCfgGet().GetView(filter);
                if (IsNotNullOrEmpty(reasonCFGs))
                {
                    MessageUtil.SetMessage(param, LibraryMessage.Message.Enum.HisExmeReasonCfg_DoiTuongChiTietVaDienDieuTriDaDuocThietLapVoiLyDoXuat, reasonCFGs.FirstOrDefault().PATIENT_CLASSIFY_NAME, reasonCFGs.FirstOrDefault().TREATMENT_TYPE_NAME, reasonCFGs.FirstOrDefault().EXP_MEST_REASON_NAME);
                    valid = false;
                }
            }
            catch (Exception ex)
            {
                LogSystem.Error(ex);
                valid = false;
                param.HasException = true;
            }
            return valid;
