        internal bool VerifyRequireField(HIS_EXME_REASON_CFG data)
        {
            bool valid = true;
            try
            {
                if (data == null) throw new ArgumentNullException("data");
                if (!IsGreaterThanZero(data.EXP_MEST_REASON_ID)) throw new ArgumentNullException("data.EXP_MEST_REASON_ID");
                if (!IsGreaterThanZero(data.PATIENT_CLASSIFY_ID)) throw new ArgumentNullException("data.PATIENT_CLASSIFY_ID");
                if (!IsGreaterThanZero(data.TREATMENT_TYPE_ID)) throw new ArgumentNullException("data.TREATMENT_TYPE_ID");
            }
            catch (ArgumentNullException ex)
            {
                BugUtil.SetBugCode(param, LibraryBug.Bug.Enum.ThieuThongTinBatBuoc);
                LogSystem.Warn(ex);
                valid = false;
            }
            catch (Exception ex)
            {
                LogSystem.Error(ex);
                valid = false;
                param.HasException = true;
            }
            return valid;
        }

