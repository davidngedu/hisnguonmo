        internal bool VerifyRequireField(HIS_CARE_TEMP data)
        {
            bool valid = true;
            try
            {
                if (data == null) throw new ArgumentNullException("data");
                if (String.IsNullOrWhiteSpace(data.CARE_TEMP_CODE)) throw new ArgumentNullException("data.CARE_TEMP_CODE");
                if (String.IsNullOrWhiteSpace(data.CARE_TEMP_NAME)) throw new ArgumentNullException("data.CARE_TEMP_NAME");
            }
            catch (ArgumentNullException ex)
            {
                BugUtil.SetBugCode(param, LibraryBug.Bug.Enum.ThieuThongTinBatBuoc);
                LogSystem.Warn(ex);
                valid = false;
            }
            catch (Exception ex)
            {
                LogSystem.Error(ex);
                valid = false;
                param.HasException = true;
            }
            return valid;
        }

