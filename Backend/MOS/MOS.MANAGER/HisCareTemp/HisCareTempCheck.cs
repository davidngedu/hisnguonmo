        internal bool IsNotDuplicate(HIS_CARE_TEMP data)
        {
            bool result = true;
            try
            {
                List<HIS_CARE_TEMP_DETAIL> careDetails = data != null ? data.HIS_CARE_TEMP_DETAIL.ToList() : null;

                if (IsNotNullOrEmpty(careDetails))
                {
                    bool check = careDetails
                        .GroupBy(o => new { o.CARE_TEMP_ID, o.CARE_TYPE_ID })
                        .Where(grp => grp.Count() > 1).Any();

                    if (check)
                    {
                        MOS.MANAGER.Base.MessageUtil.SetMessage(param, MOS.LibraryMessage.Message.Enum.HisCareTempDetail_TonTaiDuLieuTrungNhau);
                        return false;
                    }
                }
            }
            catch (Exception ex)
            {
                LogSystem.Error(ex);
                result = false;
            }
            return result;
        }

