        internal bool CheckExists(HIS_PATIENT_TYPE_ROOM data)
        {
            bool valid = true;
            try
            {
                HisPatientTypeRoomFilterQuery filter = new HisPatientTypeRoomFilterQuery();
                filter.ROOM_ID = data.ROOM_ID;
                filter.PATIENT_TYPE_ID = data.PATIENT_TYPE_ID;
                List<HIS_PATIENT_TYPE_ROOM> exists = new HisPatientTypeRoomGet().Get(filter);
                if (IsNotNullOrEmpty(exists))
                {
                    V_HIS_ROOM room = HisRoomCFG.DATA.FirstOrDefault(o => o.ID == data.ROOM_ID);
                    HIS_PATIENT_TYPE paty = HisPatientTypeCFG.DATA.FirstOrDefault(o => o.ID == data.PATIENT_TYPE_ID);
                    string roomName = room != null ? room.ROOM_NAME : "";
                    string patyName = paty != null ? paty.PATIENT_TYPE_NAME : "";

                    MANAGER.Base.MessageUtil.SetMessage(param, LibraryMessage.Message.Enum.HisPatientTypeRoom_PhongVaDoiTuongDaDuocThietLap, roomName, patyName);
                    return false;
                }
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                valid = false;
            }
            return valid;
        }
