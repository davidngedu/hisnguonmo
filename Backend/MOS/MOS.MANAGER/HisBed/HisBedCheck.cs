        internal bool CheckConstraint(long id)
        {
            bool valid = true;
            try
            {
                //HIS_TREATMENT_BED_ROOM
                //HIS_BED_TYPE
                //HIS_BED
                //HIS_BED_LOG

                List<HIS_TREATMENT_BED_ROOM> hisTreatmentBedRooms = new HisTreatmentBedRoomGet().GetByBedId(id);
