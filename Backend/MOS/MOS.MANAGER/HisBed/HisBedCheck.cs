                {
                    MOS.MANAGER.Base.MessageUtil.SetMessage(param, MOS.LibraryMessage.Message.Enum.HisBedLog_TonTaiDuLieu);
                    return false;
                }
            }
            catch (Exception ex)
            {
                LogSystem.Error(ex);
                valid = false;
                param.HasException = true;
            }
            return valid;
        }

        internal bool CheckExists(HIS_BED data)
        {
            bool valid = true;
            try
            {
                HisBedFilterQuery filter = new HisBedFilterQuery();
                filter.BED_NAME__EXACT = data.BED_NAME;
                filter.BED_ROOM_ID = data.BED_ROOM_ID;
                filter.ID__NOT_EQUAL = data.ID;
                List<HIS_BED> exists = new HisBedGet().Get(filter);
                if (IsNotNullOrEmpty(exists))
                {
                    MANAGER.Base.MessageUtil.SetMessage(param, LibraryMessage.Message.Enum.HisBed_TenGiuongDaTonTaiTrongBuong);
                    return false;
                }
            }
            catch (Exception ex)
            {
                LogSystem.Error(ex);
                valid = false;
                param.HasException = true;
            }
            return valid;
        }

        internal bool CheckExists(List<HIS_BED> data)
        {
            bool valid = true;
            try
            {
                var groups = data.GroupBy(o => new { o.BED_NAME, o.BED_ROOM_ID });
                List<string> duplicateNames = new List<string>();

                foreach (var g in groups)
                {
                    List<HIS_BED> beds = g.ToList();
                    if (beds.Count > 1)
                    {
                        duplicateNames.Add(g.Key.BED_NAME);
                    }
                }

                if (IsNotNullOrEmpty(duplicateNames))
                {
                    string s = string.Join(",", duplicateNames);
                    MOS.MANAGER.Base.MessageUtil.SetMessage(param, MOS.LibraryMessage.Message.Enum.HisBed_CacTenGiuongDaTonTaiTrongBuong, s);
                    return false;
                }
            }
            catch (Exception ex)
            {
                LogSystem.Error(ex);
                valid = false;
                param.HasException = true;
            }
            return valid;
        }
