        /// <summary>
        /// kiểm tra dữ liệu tạo thông tin báo động
        /// </summary>
        /// <param name="id"></param>
        /// <param name="data"></param>
        /// <returns></returns>
        internal bool IsAllow(HisAlertSDO sdo, WorkPlaceSDO workPlaceSdo)
        {
            bool valid = true;
            try
            {
                var departmentNotHasEmployee = sdo.ReceiveDepartmentIds.FirstOrDefault(o => !HisEmployeeCFG.DATA.Exists(p => p.DEPARTMENT_ID == o));
                if (departmentNotHasEmployee > 0)
                {
                    HIS_DEPARTMENT department = HisDepartmentCFG.DATA.FirstOrDefault(o => o.ID == departmentNotHasEmployee);
                    MOS.MANAGER.Base.MessageUtil.SetMessage(param, MOS.LibraryMessage.Message.Enum.KhongTimThayNhanVienLamViecTai, department.DEPARTMENT_NAME);
                    return false;
                }

                if (workPlaceSdo.RoomId != sdo.RequestRoomId)
                {
                    V_HIS_ROOM requestRoom = HisRoomCFG.DATA.Where(o => o.ID == sdo.RequestRoomId).FirstOrDefault();
                    if (requestRoom != null)
                    {
                        MOS.MANAGER.Base.MessageUtil.SetMessage(param, MOS.LibraryMessage.Message.Enum.BanDangKhongLamViecTaiPhong, requestRoom.ROOM_NAME);
                        return false;
                    }
                }
            }
            catch (Exception ex)
            {
                LogSystem.Error(ex);
                valid = false;
                param.HasException = true;
            }
            return valid;
        }

        internal bool VerifyRequireField1(HisAlertSDO data)
        {
            bool valid = true;
            try
