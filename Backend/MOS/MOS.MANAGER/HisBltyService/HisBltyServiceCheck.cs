        internal bool VerifyRequireField(HIS_BLTY_SERVICE data)
        {
            bool valid = true;
            try
            {
                if (data == null) throw new ArgumentNullException("data");
                if (data.BLOOD_TYPE_ID <= 0) throw new ArgumentNullException("data.BLOOD_TYPE_ID");
                if (data.SERVICE_ID <= 0) throw new ArgumentNullException("data.SERVICE_ID");
                if (data.BLTY_AMOUNT <= 0) throw new ArgumentNullException("data.BLTY_AMOUNT");
                if (data.SERVICE_AMOUNT < 0) throw new ArgumentNullException("data.SERVICE_AMOUNT");
            }
            catch (ArgumentNullException ex)
            {
                BugUtil.SetBugCode(param, LibraryBug.Bug.Enum.ThieuThongTinBatBuoc);
                LogSystem.Warn(ex);
                valid = false;
            }
            catch (Exception ex)
            {
                LogSystem.Error(ex);
                valid = false;
                param.HasException = true;
            }
            return valid;
        }

