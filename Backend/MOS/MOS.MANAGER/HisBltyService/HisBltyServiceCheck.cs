        internal bool IsNotExists(HIS_BLTY_SERVICE data)
        {
            bool valid = true;
            try
            {
                List<HIS_BLTY_SERVICE> exists = DAOWorker.SqlDAO.GetSql<HIS_BLTY_SERVICE>("SELECT * FROM HIS_BLTY_SERVICE WHERE BLOOD_TYPE_ID = :param1 AND SERVICE_ID = :param2 AND BLTY_AMOUNT = :param3 AND ID <> :param4", data.BLOOD_TYPE_ID, data.SERVICE_ID, data.BLTY_AMOUNT, data.ID);
                if (IsNotNullOrEmpty(exists))
                {
                    HIS_BLOOD_TYPE blty = HisBloodTypeCFG.DATA.FirstOrDefault(o => o.ID == data.BLOOD_TYPE_ID);
                    string bltyName = blty != null ? blty.BLOOD_TYPE_NAME : "";
                    V_HIS_SERVICE service = HisServiceCFG.DATA_VIEW.FirstOrDefault(o => o.ID == data.SERVICE_ID);
                    string serviceName = service != null ? service.SERVICE_NAME : "";
                    MANAGER.Base.MessageUtil.SetMessage(param, LibraryMessage.Message.Enum.HisBltyService_TonTaiDuLieu, bltyName, data.BLTY_AMOUNT.ToString(), serviceName);
                    return false;
                }
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                valid = false;
                param.HasException = true;
            }
            return valid;
        }
