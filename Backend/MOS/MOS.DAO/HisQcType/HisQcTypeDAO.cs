        public List<HIS_QC_TYPE> Get(HisQcTypeSO search, CommonParam param)
        {
            List<HIS_QC_TYPE> result = new List<HIS_QC_TYPE>();
            try
            {
                result = GetWorker.Get(search, param);
            }
            catch (Exception ex)
            {
                param.HasException = true;
                Inventec.Common.Logging.LogSystem.Error(ex);
                result.Clear();
            }
            return result;
        }

