namespace MOS.DAO.HisConfig
{
    public partial class HisConfigDAO : EntityBase
    {
        private HisConfigCreate CreateWorker
        {
            get
            {
                return (HisConfigCreate)Worker.Get<HisConfigCreate>();
            }
        }
        private HisConfigUpdate UpdateWorker
        {
            get
            {
