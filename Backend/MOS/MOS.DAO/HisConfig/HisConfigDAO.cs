            }
        }
        private HisConfigDelete DeleteWorker
        {
            get
            {
                return (HisConfigDelete)Worker.Get<HisConfigDelete>();
            }
        }
        private HisConfigTruncate TruncateWorker
        {
            get
            {
                return (HisConfigTruncate)Worker.Get<HisConfigTruncate>();
            }
        }
        private HisConfigGet GetWorker
        {
            get
            {
                return (HisConfigGet)Worker.Get<HisConfigGet>();
            }
        }
        private HisConfigCheck CheckWorker
        {
            get
            {
                return (HisConfigCheck)Worker.Get<HisConfigCheck>();
            }
        }

        public bool Create(HIS_CONFIG data)
        {
            bool result = false;
            try
            {
                result = CreateWorker.Create(data);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = false;
            }
            return result;
        }

        public bool CreateList(List<HIS_CONFIG> listData)
        {
            bool result = false;
            try
            {
                result = CreateWorker.CreateList(listData);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = false;
            }
            return result;
        }

        public bool Update(HIS_CONFIG data)
        {
            bool result = false;
