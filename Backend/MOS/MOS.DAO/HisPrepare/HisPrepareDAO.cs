        public List<HIS_PREPARE> Get(HisPrepareSO search, CommonParam param)
        {
            List<HIS_PREPARE> result = new List<HIS_PREPARE>();
            try
            {
                result = GetWorker.Get(search, param);
            }
            catch (Exception ex)
            {
                param.HasException = true;
                Inventec.Common.Logging.LogSystem.Error(ex);
                result.Clear();
            }
            return result;
        }

        public HIS_PREPARE GetById(long id, HisPrepareSO search)
        {
            HIS_PREPARE result = null;
            try
            {
                result = GetWorker.GetById(id, search);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = null;
            }

            return result;
        }

        public bool IsUnLock(long id)
