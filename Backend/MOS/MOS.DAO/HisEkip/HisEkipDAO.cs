namespace MOS.DAO.HisEkip
{
    public partial class HisEkipDAO : EntityBase
    {
        private HisEkipCreate CreateWorker
        {
            get
            {
                return (HisEkipCreate)Worker.Get<HisEkipCreate>();
            }
        }
        private HisEkipUpdate UpdateWorker
        {
            get
            {
