            }
        }
        private HisEkipGet GetWorker
        {
            get
            {
                return (HisEkipGet)Worker.Get<HisEkipGet>();
            }
        }
        private HisEkipCheck CheckWorker
        {
            get
            {
                return (HisEkipCheck)Worker.Get<HisEkipCheck>();
            }
        }

        public bool Create(HIS_EKIP data)
        {
            bool result = false;
            try
            {
                result = CreateWorker.Create(data);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = false;
            }
            return result;
        }

        public bool CreateList(List<HIS_EKIP> listData)
        {
            bool result = false;
