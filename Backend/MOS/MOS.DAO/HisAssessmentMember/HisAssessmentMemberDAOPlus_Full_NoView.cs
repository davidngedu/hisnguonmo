using MOS.DAO.StagingObject;
using MOS.EFMODEL.DataModels;
using Inventec.Core;
using System;
using System.Collections.Generic;

namespace MOS.DAO.HisAssessmentMember
{
    public partial class HisAssessmentMemberDAO : EntityBase
    {
        public HIS_ASSESSMENT_MEMBER GetByCode(string code, HisAssessmentMemberSO search)
        {
            HIS_ASSESSMENT_MEMBER result = null;

            try
            {
                result = GetWorker.GetByCode(code, search);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = null;
            }

            return result;
        }

        public Dictionary<string, HIS_ASSESSMENT_MEMBER> GetDicByCode(HisAssessmentMemberSO search, CommonParam param)
        {
            Dictionary<string, HIS_ASSESSMENT_MEMBER> result = new Dictionary<string, HIS_ASSESSMENT_MEMBER>();
            try
            {
                result = GetWorker.GetDicByCode(search, param);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result.Clear();
            }

            return result;
        }

        public bool ExistsCode(string code, long? id)
        {

            try
            {
                return CheckWorker.ExistsCode(code, id);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                throw;
            }
        }
    }
}
