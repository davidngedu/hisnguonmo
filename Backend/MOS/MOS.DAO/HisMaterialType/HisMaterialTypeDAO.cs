        public List<HIS_MATERIAL_TYPE> Get(HisMaterialTypeSO search, CommonParam param)
        {
            List<HIS_MATERIAL_TYPE> result = new List<HIS_MATERIAL_TYPE>();
            try
            {
                result = GetWorker.Get(search, param);
