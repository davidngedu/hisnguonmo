using MOS.DAO.StagingObject;
using MOS.EFMODEL.DataModels;
using Inventec.Core;
using System;
using System.Collections.Generic;

namespace MOS.DAO.HisSupplier
{
    public partial class HisSupplierDAO : EntityBase
    {
        public List<V_HIS_SUPPLIER> GetView(HisSupplierSO search, CommonParam param)
        {
            List<V_HIS_SUPPLIER> result = new List<V_HIS_SUPPLIER>();

            try
            {
                result = GetWorker.GetView(search, param);
            }
            catch (Exception ex)
            {
                param.HasException = true;
                Inventec.Common.Logging.LogSystem.Error(ex);
                result.Clear();
            }

            return result;
        }

        public HIS_SUPPLIER GetByCode(string code, HisSupplierSO search)
        {
            HIS_SUPPLIER result = null;

            try
            {
                result = GetWorker.GetByCode(code, search);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = null;
            }

            return result;
        }
        
        public V_HIS_SUPPLIER GetViewById(long id, HisSupplierSO search)
        {
            V_HIS_SUPPLIER result = null;

            try
            {
                result = GetWorker.GetViewById(id, search);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = null;
            }

            return result;
        }

        public V_HIS_SUPPLIER GetViewByCode(string code, HisSupplierSO search)
        {
            V_HIS_SUPPLIER result = null;

            try
            {
                result = GetWorker.GetViewByCode(code, search);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = null;
            }
            return result;
        }

        public Dictionary<string, HIS_SUPPLIER> GetDicByCode(HisSupplierSO search, CommonParam param)
        {
            Dictionary<string, HIS_SUPPLIER> result = new Dictionary<string, HIS_SUPPLIER>();
            try
            {
                result = GetWorker.GetDicByCode(search, param);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result.Clear();
            }

            return result;
        }

        public bool ExistsCode(string code, long? id)
        {
            try
            {
                return CheckWorker.ExistsCode(code, id);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                throw;
            }
        }
    }
}
