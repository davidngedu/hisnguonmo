namespace MOS.DAO.HisBid
{
    public partial class HisBidDAO : EntityBase
    {
        private HisBidCreate CreateWorker
        {
            get
            {
                return (HisBidCreate)Worker.Get<HisBidCreate>();
            }
        }
        private HisBidUpdate UpdateWorker
        {
            get
            {
                return (HisBidUpdate)Worker.Get<HisBidUpdate>();
