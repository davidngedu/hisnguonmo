        {
            get
            {
                return (HisBidTruncate)Worker.Get<HisBidTruncate>();
            }
        }
        private HisBidGet GetWorker
        {
            get
            {
                return (HisBidGet)Worker.Get<HisBidGet>();
            }
        }
        private HisBidCheck CheckWorker
        {
            get
            {
                return (HisBidCheck)Worker.Get<HisBidCheck>();
            }
        }

        public bool Create(HIS_BID data)
        {
            bool result = false;
            try
            {
                result = CreateWorker.Create(data);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = false;
            }
            return result;
        }

        public bool CreateList(List<HIS_BID> listData)
        {
            bool result = false;
