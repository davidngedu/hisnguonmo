using MOS.DAO.StagingObject;
using MOS.EFMODEL.DataModels;
using Inventec.Core;
using System;
using System.Collections.Generic;

namespace MOS.DAO.HisLicenseClass
{
    public partial class HisLicenseClassDAO : EntityBase
    {
        public HIS_LICENSE_CLASS GetByCode(string code, HisLicenseClassSO search)
        {
            HIS_LICENSE_CLASS result = null;

            try
            {
                result = GetWorker.GetByCode(code, search);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = null;
            }

            return result;
        }

        public Dictionary<string, HIS_LICENSE_CLASS> GetDicByCode(HisLicenseClassSO search, CommonParam param)
        {
            Dictionary<string, HIS_LICENSE_CLASS> result = new Dictionary<string, HIS_LICENSE_CLASS>();
            try
            {
                result = GetWorker.GetDicByCode(search, param);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result.Clear();
            }

            return result;
        }

        public bool ExistsCode(string code, long? id)
        {

            try
            {
                return CheckWorker.ExistsCode(code, id);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                throw;
            }
        }
    }
}
