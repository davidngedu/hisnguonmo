        public List<HIS_ANTIGEN> Get(HisAntigenSO search, CommonParam param)
        {
            List<HIS_ANTIGEN> result = new List<HIS_ANTIGEN>();
            try
            {
                result = GetWorker.Get(search, param);
            }
            catch (Exception ex)
            {
                param.HasException = true;
                Inventec.Common.Logging.LogSystem.Error(ex);
                result.Clear();
            }
            return result;
        }

        public HIS_ANTIGEN GetById(long id, HisAntigenSO search)
        {
            HIS_ANTIGEN result = null;
            try
            {
                result = GetWorker.GetById(id, search);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = null;
            }

            return result;
        }

