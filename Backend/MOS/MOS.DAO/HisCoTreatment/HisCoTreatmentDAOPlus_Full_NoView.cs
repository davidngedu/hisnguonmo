using MOS.DAO.StagingObject;
using MOS.EFMODEL.DataModels;
using Inventec.Core;
using System;
using System.Collections.Generic;

namespace MOS.DAO.HisCoTreatment
{
    public partial class HisCoTreatmentDAO : EntityBase
    {
        public HIS_CO_TREATMENT GetByCode(string code, HisCoTreatmentSO search)
        {
            HIS_CO_TREATMENT result = null;

            try
            {
                result = GetWorker.GetByCode(code, search);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = null;
            }

            return result;
        }

        public Dictionary<string, HIS_CO_TREATMENT> GetDicByCode(HisCoTreatmentSO search, CommonParam param)
        {
            Dictionary<string, HIS_CO_TREATMENT> result = new Dictionary<string, HIS_CO_TREATMENT>();
            try
            {
                result = GetWorker.GetDicByCode(search, param);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result.Clear();
            }

            return result;
        }

        public bool ExistsCode(string code, long? id)
        {

            try
            {
                return CheckWorker.ExistsCode(code, id);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                throw;
            }
        }
    }
}
