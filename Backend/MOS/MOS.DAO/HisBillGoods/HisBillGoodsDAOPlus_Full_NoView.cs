using MOS.DAO.StagingObject;
using MOS.EFMODEL.DataModels;
using Inventec.Core;
using System;
using System.Collections.Generic;

namespace MOS.DAO.HisBillGoods
{
    public partial class HisBillGoodsDAO : EntityBase
    {
        public HIS_BILL_GOODS GetByCode(string code, HisBillGoodsSO search)
        {
            HIS_BILL_GOODS result = null;

            try
            {
                result = GetWorker.GetByCode(code, search);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = null;
            }

            return result;
        }

        public Dictionary<string, HIS_BILL_GOODS> GetDicByCode(HisBillGoodsSO search, CommonParam param)
        {
            Dictionary<string, HIS_BILL_GOODS> result = new Dictionary<string, HIS_BILL_GOODS>();
            try
            {
                result = GetWorker.GetDicByCode(search, param);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result.Clear();
            }

            return result;
        }

        public bool ExistsCode(string code, long? id)
        {

            try
            {
                return CheckWorker.ExistsCode(code, id);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                throw;
            }
        }
    }
}
