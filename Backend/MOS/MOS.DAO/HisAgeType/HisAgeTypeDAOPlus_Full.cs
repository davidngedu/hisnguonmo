using MOS.DAO.StagingObject;
using MOS.EFMODEL.DataModels;
using Inventec.Core;
using System;
using System.Collections.Generic;

namespace MOS.DAO.HisAgeType
{
    public partial class HisAgeTypeDAO : EntityBase
    {
        public List<V_HIS_AGE_TYPE> GetView(HisAgeTypeSO search, CommonParam param)
        {
            List<V_HIS_AGE_TYPE> result = new List<V_HIS_AGE_TYPE>();

            try
            {
                result = GetWorker.GetView(search, param);
            }
            catch (Exception ex)
            {
                param.HasException = true;
                Inventec.Common.Logging.LogSystem.Error(ex);
                result.Clear();
            }

            return result;
        }

        public HIS_AGE_TYPE GetByCode(string code, HisAgeTypeSO search)
        {
            HIS_AGE_TYPE result = null;

            try
            {
                result = GetWorker.GetByCode(code, search);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = null;
            }

            return result;
        }
        
        public V_HIS_AGE_TYPE GetViewById(long id, HisAgeTypeSO search)
        {
            V_HIS_AGE_TYPE result = null;

            try
            {
                result = GetWorker.GetViewById(id, search);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = null;
            }

            return result;
        }

        public V_HIS_AGE_TYPE GetViewByCode(string code, HisAgeTypeSO search)
        {
            V_HIS_AGE_TYPE result = null;

            try
            {
                result = GetWorker.GetViewByCode(code, search);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = null;
            }
            return result;
        }

        public Dictionary<string, HIS_AGE_TYPE> GetDicByCode(HisAgeTypeSO search, CommonParam param)
        {
            Dictionary<string, HIS_AGE_TYPE> result = new Dictionary<string, HIS_AGE_TYPE>();
            try
            {
                result = GetWorker.GetDicByCode(search, param);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result.Clear();
            }

            return result;
        }

        public bool ExistsCode(string code, long? id)
        {
            try
            {
                return CheckWorker.ExistsCode(code, id);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                throw;
            }
        }
