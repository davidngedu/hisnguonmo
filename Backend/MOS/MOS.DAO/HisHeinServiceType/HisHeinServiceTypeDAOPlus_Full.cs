using MOS.DAO.StagingObject;
using MOS.EFMODEL.DataModels;
using Inventec.Core;
using System;
using System.Collections.Generic;

namespace MOS.DAO.HisServiceReport
{
    public partial class HisServiceReportDAO : EntityBase
    {
        public List<V_HIS_SERVICE_REPORT> GetView(HisServiceReportSO search, CommonParam param)
        {
            List<V_HIS_SERVICE_REPORT> result = new List<V_HIS_SERVICE_REPORT>();

            try
            {
                result = GetWorker.GetView(search, param);
            }
            catch (Exception ex)
            {
                param.HasException = true;
                Inventec.Common.Logging.LogSystem.Error(ex);
                result.Clear();
            }

            return result;
        }

        public HIS_SERVICE_REPORT GetByCode(string code, HisServiceReportSO search)
        {
            HIS_SERVICE_REPORT result = null;

            try
            {
                result = GetWorker.GetByCode(code, search);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = null;
            }

            return result;
        }
        
        public V_HIS_SERVICE_REPORT GetViewById(long id, HisServiceReportSO search)
        {
            V_HIS_SERVICE_REPORT result = null;

            try
            {
                result = GetWorker.GetViewById(id, search);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = null;
            }

            return result;
        }

        public V_HIS_SERVICE_REPORT GetViewByCode(string code, HisServiceReportSO search)
        {
            V_HIS_SERVICE_REPORT result = null;

            try
            {
                result = GetWorker.GetViewByCode(code, search);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = null;
            }
            return result;
        }

        public Dictionary<string, HIS_SERVICE_REPORT> GetDicByCode(HisServiceReportSO search, CommonParam param)
        {
            Dictionary<string, HIS_SERVICE_REPORT> result = new Dictionary<string, HIS_SERVICE_REPORT>();
            try
            {
                result = GetWorker.GetDicByCode(search, param);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result.Clear();
            }

            return result;
        }

        public bool ExistsCode(string code, long? id)
        {
            try
            {
                return CheckWorker.ExistsCode(code, id);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                throw;
            }
        }
    }
}
