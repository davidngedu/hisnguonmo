using Inventec.Core;
using System;
using System.Collections.Generic;

namespace MOS.DAO.HisAdr
{
    public partial class HisAdrDAO : EntityBase
    {
        private HisAdrCreate CreateWorker
        {
            get
            {
                return (HisAdrCreate)Worker.Get<HisAdrCreate>();
            }
        }
        private HisAdrUpdate UpdateWorker
        {
            get
            {
                return (HisAdrUpdate)Worker.Get<HisAdrUpdate>();
            }
        }
