            {
                return (HisAdrDelete)Worker.Get<HisAdrDelete>();
            }
        }
        private HisAdrTruncate TruncateWorker
        {
            get
            {
                return (HisAdrTruncate)Worker.Get<HisAdrTruncate>();
            }
        }
        private HisAdrGet GetWorker
        {
            get
            {
                return (HisAdrGet)Worker.Get<HisAdrGet>();
            }
        }
        private HisAdrCheck CheckWorker
        {
            get
            {
                return (HisAdrCheck)Worker.Get<HisAdrCheck>();
            }
        }

        public bool Create(HIS_ADR data)
        {
            bool result = false;
            try
            {
                result = CreateWorker.Create(data);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = false;
            }
            return result;
        }

        public bool CreateList(List<HIS_ADR> listData)
        {
            bool result = false;
            try
            {
                result = CreateWorker.CreateList(listData);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = false;
            }
            return result;
        }

        public bool Update(HIS_ADR data)
        {
            bool result = false;
            try
            {
                result = UpdateWorker.Update(data);
