namespace MOS.DAO.HisDhst
{
    public partial class HisDhstDAO : EntityBase
    {
        private HisDhstCreate CreateWorker
        {
            get
            {
                return (HisDhstCreate)Worker.Get<HisDhstCreate>();
            }
        }
        private HisDhstUpdate UpdateWorker
        {
