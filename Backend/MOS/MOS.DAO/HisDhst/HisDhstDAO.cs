            }
        }
        private HisDhstGet GetWorker
        {
            get
            {
                return (HisDhstGet)Worker.Get<HisDhstGet>();
            }
        }
        private HisDhstCheck CheckWorker
        {
            get
            {
                return (HisDhstCheck)Worker.Get<HisDhstCheck>();
            }
        }

        public bool Create(HIS_DHST data)
        {
            bool result = false;
            try
            {
                result = CreateWorker.Create(data);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = false;
            }
            return result;
        }

        public bool CreateList(List<HIS_DHST> listData)
        {
            bool result = false;
