        {
            get
            {
                return (HisHtuGet)Worker.Get<HisHtuGet>();
            }
        }
        private HisHtuCheck CheckWorker
        {
            get
            {
                return (HisHtuCheck)Worker.Get<HisHtuCheck>();
            }
