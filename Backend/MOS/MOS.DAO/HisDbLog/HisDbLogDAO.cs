        public List<HIS_DB_LOG> Get(HisDbLogSO search, CommonParam param)
        {
            List<HIS_DB_LOG> result = new List<HIS_DB_LOG>();
            try
            {
                result = GetWorker.Get(search, param);
            }
            catch (Exception ex)
            {
                param.HasException = true;
                Inventec.Common.Logging.LogSystem.Error(ex);
                result.Clear();
            }
            return result;
        }

        public HIS_DB_LOG GetById(long id, HisDbLogSO search)
        {
            HIS_DB_LOG result = null;
            try
            {
                result = GetWorker.GetById(id, search);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = null;
            }

            return result;
        }

