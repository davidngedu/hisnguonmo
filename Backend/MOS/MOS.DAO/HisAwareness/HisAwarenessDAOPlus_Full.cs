using MOS.DAO.StagingObject;
using MOS.EFMODEL.DataModels;
using Inventec.Core;
using System;
using System.Collections.Generic;

namespace MOS.DAO.HisAwareness
{
    public partial class HisAwarenessDAO : EntityBase
    {
        public List<V_HIS_AWARENESS> GetView(HisAwarenessSO search, CommonParam param)
        {
            List<V_HIS_AWARENESS> result = new List<V_HIS_AWARENESS>();

            try
            {
                result = GetWorker.GetView(search, param);
            }
            catch (Exception ex)
            {
                param.HasException = true;
                Inventec.Common.Logging.LogSystem.Error(ex);
                result.Clear();
            }

            return result;
        }

        public HIS_AWARENESS GetByCode(string code, HisAwarenessSO search)
        {
            HIS_AWARENESS result = null;

            try
            {
                result = GetWorker.GetByCode(code, search);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = null;
            }

            return result;
        }
        
        public V_HIS_AWARENESS GetViewById(long id, HisAwarenessSO search)
        {
            V_HIS_AWARENESS result = null;

            try
            {
                result = GetWorker.GetViewById(id, search);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = null;
            }

            return result;
        }

        public V_HIS_AWARENESS GetViewByCode(string code, HisAwarenessSO search)
        {
            V_HIS_AWARENESS result = null;

            try
            {
                result = GetWorker.GetViewByCode(code, search);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = null;
            }
            return result;
        }

        public Dictionary<string, HIS_AWARENESS> GetDicByCode(HisAwarenessSO search, CommonParam param)
        {
            Dictionary<string, HIS_AWARENESS> result = new Dictionary<string, HIS_AWARENESS>();
            try
            {
                result = GetWorker.GetDicByCode(search, param);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result.Clear();
            }

            return result;
        }

        public bool ExistsCode(string code, long? id)
        {
            try
            {
                return CheckWorker.ExistsCode(code, id);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                throw;
            }
        }
    }
}
