        public List<HIS_PATIENT_CASE> Get(HisPatientCaseSO search, CommonParam param)
        {
            List<HIS_PATIENT_CASE> result = new List<HIS_PATIENT_CASE>();
            try
            {
                result = GetWorker.Get(search, param);
