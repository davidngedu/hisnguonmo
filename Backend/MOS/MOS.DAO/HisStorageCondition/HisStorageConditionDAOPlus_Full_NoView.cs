using MOS.DAO.StagingObject;
using MOS.EFMODEL.DataModels;
using Inventec.Core;
using System;
using System.Collections.Generic;

namespace MOS.DAO.HisStorageCondition
{
    public partial class HisStorageConditionDAO : EntityBase
    {
        public HIS_STORAGE_CONDITION GetByCode(string code, HisStorageConditionSO search)
        {
            HIS_STORAGE_CONDITION result = null;

            try
            {
                result = GetWorker.GetByCode(code, search);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = null;
            }

            return result;
        }

        public Dictionary<string, HIS_STORAGE_CONDITION> GetDicByCode(HisStorageConditionSO search, CommonParam param)
        {
            Dictionary<string, HIS_STORAGE_CONDITION> result = new Dictionary<string, HIS_STORAGE_CONDITION>();
            try
            {
                result = GetWorker.GetDicByCode(search, param);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result.Clear();
            }

            return result;
        }

        public bool ExistsCode(string code, long? id)
        {

            try
            {
                return CheckWorker.ExistsCode(code, id);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                throw;
            }
        }
    }
}
