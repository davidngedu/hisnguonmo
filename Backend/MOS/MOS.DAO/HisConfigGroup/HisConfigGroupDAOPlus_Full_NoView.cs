using MOS.DAO.StagingObject;
using MOS.EFMODEL.DataModels;
using Inventec.Core;
using System;
using System.Collections.Generic;

namespace MOS.DAO.HisConfigGroup
{
    public partial class HisConfigGroupDAO : EntityBase
    {
        public HIS_CONFIG_GROUP GetByCode(string code, HisConfigGroupSO search)
        {
            HIS_CONFIG_GROUP result = null;

            try
            {
                result = GetWorker.GetByCode(code, search);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = null;
            }

            return result;
        }

        public Dictionary<string, HIS_CONFIG_GROUP> GetDicByCode(HisConfigGroupSO search, CommonParam param)
        {
            Dictionary<string, HIS_CONFIG_GROUP> result = new Dictionary<string, HIS_CONFIG_GROUP>();
            try
            {
                result = GetWorker.GetDicByCode(search, param);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result.Clear();
            }

            return result;
        }

        public bool ExistsCode(string code, long? id)
        {

            try
            {
                return CheckWorker.ExistsCode(code, id);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                throw;
            }
        }
    }
}
