using MOS.DAO.StagingObject;
using MOS.EFMODEL.DataModels;
using Inventec.Core;
using System;
using System.Collections.Generic;

namespace MOS.DAO.HisMedicineGroup
{
    public partial class HisMedicineGroupDAO : EntityBase
    {
        public HIS_MEDICINE_GROUP GetByCode(string code, HisMedicineGroupSO search)
        {
            HIS_MEDICINE_GROUP result = null;

            try
            {
                result = GetWorker.GetByCode(code, search);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = null;
            }

            return result;
        }

        public Dictionary<string, HIS_MEDICINE_GROUP> GetDicByCode(HisMedicineGroupSO search, CommonParam param)
        {
            Dictionary<string, HIS_MEDICINE_GROUP> result = new Dictionary<string, HIS_MEDICINE_GROUP>();
            try
            {
                result = GetWorker.GetDicByCode(search, param);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result.Clear();
            }

            return result;
        }

        public bool ExistsCode(string code, long? id)
        {

            try
            {
                return CheckWorker.ExistsCode(code, id);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                throw;
            }
        }
    }
}
