        {
            get
            {
                return (HisAtcGet)Worker.Get<HisAtcGet>();
            }
        }
        private HisAtcCheck CheckWorker
        {
            get
            {
                return (HisAtcCheck)Worker.Get<HisAtcCheck>();
            }
