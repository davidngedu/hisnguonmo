        public List<HIS_MEDI_REACT> Get(HisMediReactSO search, CommonParam param)
        {
            List<HIS_MEDI_REACT> result = new List<HIS_MEDI_REACT>();
            try
            {
                result = GetWorker.Get(search, param);
            }
            catch (Exception ex)
            {
