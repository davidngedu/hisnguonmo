using MOS.DAO.StagingObject;
using MOS.EFMODEL.DataModels;
using Inventec.Core;
using System;
using System.Collections.Generic;

namespace MOS.DAO.HisDebateUser
{
    public partial class HisDebateUserDAO : EntityBase
    {
        public List<V_HIS_DEBATE_USER> GetView(HisDebateUserSO search, CommonParam param)
        {
            List<V_HIS_DEBATE_USER> result = new List<V_HIS_DEBATE_USER>();

            try
            {
                result = GetWorker.GetView(search, param);
            }
            catch (Exception ex)
            {
                param.HasException = true;
                Inventec.Common.Logging.LogSystem.Error(ex);
                result.Clear();
            }

            return result;
        }

        public HIS_DEBATE_USER GetByCode(string code, HisDebateUserSO search)
        {
            HIS_DEBATE_USER result = null;

            try
            {
                result = GetWorker.GetByCode(code, search);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = null;
            }

            return result;
        }
        
        public V_HIS_DEBATE_USER GetViewById(long id, HisDebateUserSO search)
        {
            V_HIS_DEBATE_USER result = null;

            try
            {
                result = GetWorker.GetViewById(id, search);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = null;
            }

            return result;
        }

        public V_HIS_DEBATE_USER GetViewByCode(string code, HisDebateUserSO search)
        {
            V_HIS_DEBATE_USER result = null;

            try
            {
                result = GetWorker.GetViewByCode(code, search);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = null;
            }
            return result;
        }

        public Dictionary<string, HIS_DEBATE_USER> GetDicByCode(HisDebateUserSO search, CommonParam param)
        {
            Dictionary<string, HIS_DEBATE_USER> result = new Dictionary<string, HIS_DEBATE_USER>();
            try
            {
                result = GetWorker.GetDicByCode(search, param);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result.Clear();
            }

            return result;
        }

        public bool ExistsCode(string code, long? id)
        {
            try
            {
                return CheckWorker.ExistsCode(code, id);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                throw;
            }
        }
