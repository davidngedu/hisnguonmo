        {
            get
            {
                return (HisRefectoryGet)Worker.Get<HisRefectoryGet>();
            }
        }
        private HisRefectoryCheck CheckWorker
        {
            get
            {
                return (HisRefectoryCheck)Worker.Get<HisRefectoryCheck>();
