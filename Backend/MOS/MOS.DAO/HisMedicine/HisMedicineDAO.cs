        {
            get
            {
                return (HisMedicineGet)Worker.Get<HisMedicineGet>();
            }
        }
        private HisMedicineCheck CheckWorker
        {
            get
            {
                return (HisMedicineCheck)Worker.Get<HisMedicineCheck>();
            }
