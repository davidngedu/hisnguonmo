        {
            get
            {
                return (HisFileTypeGet)Worker.Get<HisFileTypeGet>();
            }
        }
        private HisFileTypeCheck CheckWorker
        {
            get
            {
                return (HisFileTypeCheck)Worker.Get<HisFileTypeCheck>();
            }
        }

        public bool Create(HIS_FILE_TYPE data)
        {
            bool result = false;
            try
            {
                result = CreateWorker.Create(data);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = false;
            }
            return result;
        }

