        {
            get
            {
                return (HisDeskTruncate)Worker.Get<HisDeskTruncate>();
            }
        }
        private HisDeskGet GetWorker
        {
            get
            {
                return (HisDeskGet)Worker.Get<HisDeskGet>();
            }
        }
        private HisDeskCheck CheckWorker
        {
            get
            {
                return (HisDeskCheck)Worker.Get<HisDeskCheck>();
            }
        }

        public bool Create(HIS_DESK data)
        {
            bool result = false;
            try
            {
                result = CreateWorker.Create(data);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = false;
            }
            return result;
        }

        public bool CreateList(List<HIS_DESK> listData)
        {
            bool result = false;
