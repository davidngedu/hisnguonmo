namespace MOS.DAO.HisDesk
{
    public partial class HisDeskDAO : EntityBase
    {
        private HisDeskCreate CreateWorker
        {
            get
            {
                return (HisDeskCreate)Worker.Get<HisDeskCreate>();
            }
        }
        private HisDeskUpdate UpdateWorker
        {
            get
            {
                return (HisDeskUpdate)Worker.Get<HisDeskUpdate>();
            }
        }
