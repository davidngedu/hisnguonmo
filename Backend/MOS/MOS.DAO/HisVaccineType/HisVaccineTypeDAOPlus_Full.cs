using MOS.DAO.StagingObject;
using MOS.EFMODEL.DataModels;
using Inventec.Core;
using System;
using System.Collections.Generic;

namespace MOS.DAO.HisVaccineType
{
    public partial class HisVaccineTypeDAO : EntityBase
    {
        public HIS_VACCINE_TYPE GetByCode(string code, HisVaccineTypeSO search)
        {
            HIS_VACCINE_TYPE result = null;

            try
            {
                result = GetWorker.GetByCode(code, search);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = null;
            }

            return result;
        }
        

        public Dictionary<string, HIS_VACCINE_TYPE> GetDicByCode(HisVaccineTypeSO search, CommonParam param)
        {
            Dictionary<string, HIS_VACCINE_TYPE> result = new Dictionary<string, HIS_VACCINE_TYPE>();
            try
            {
                result = GetWorker.GetDicByCode(search, param);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result.Clear();
            }

            return result;
        }

        public bool ExistsCode(string code, long? id)
        {
            try
            {
                return CheckWorker.ExistsCode(code, id);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                throw;
            }
        }
    }
}
