        public List<HIS_PERMISSION> Get(HisPermissionSO search, CommonParam param)
        {
            List<HIS_PERMISSION> result = new List<HIS_PERMISSION>();
            try
            {
                result = GetWorker.Get(search, param);
            }
            catch (Exception ex)
            {
                param.HasException = true;
                Inventec.Common.Logging.LogSystem.Error(ex);
                result.Clear();
            }
            return result;
        }

        public HIS_PERMISSION GetById(long id, HisPermissionSO search)
