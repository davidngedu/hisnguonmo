using MOS.DAO.StagingObject;
using MOS.EFMODEL.DataModels;
using Inventec.Core;
using System;
using System.Collections.Generic;

namespace MOS.DAO.HisAccidentHurt
{
    public partial class HisAccidentHurtDAO : EntityBase
    {
        public HIS_ACCIDENT_HURT GetByCode(string code, HisAccidentHurtSO search)
        {
            HIS_ACCIDENT_HURT result = null;

            try
            {
                result = GetWorker.GetByCode(code, search);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result = null;
            }

            return result;
        }

        public Dictionary<string, HIS_ACCIDENT_HURT> GetDicByCode(HisAccidentHurtSO search, CommonParam param)
        {
            Dictionary<string, HIS_ACCIDENT_HURT> result = new Dictionary<string, HIS_ACCIDENT_HURT>();
            try
            {
                result = GetWorker.GetDicByCode(search, param);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                result.Clear();
            }

            return result;
        }

        public bool ExistsCode(string code, long? id)
        {

            try
            {
                return CheckWorker.ExistsCode(code, id);
            }
            catch (Exception ex)
            {
                Inventec.Common.Logging.LogSystem.Error(ex);
                throw;
            }
        }
    }
}
