using SDA.DAO.Base;
using SDA.DAO.StagingObject;
using SDA.EFMODEL.DataModels;
using Inventec.Common.Logging;
using Inventec.Core;
using System;
using System.Collections.Generic;
using System.Linq;

namespace SDA.DAO.SdaReligion
{
    partial class SdaReligionGet : EntityBase
    {
        public Dictionary<string, SDA_RELIGION> GetDicByCode(SdaReligionSO search, CommonParam param)
        {
            Dictionary<string, SDA_RELIGION> dic = new Dictionary<string, SDA_RELIGION>();
            try
            {
                List<SDA_RELIGION> listRecord = Get(search, param);
                if (listRecord != null)
                {
                    foreach (var item in listRecord)
                    {
                        if (!dic.ContainsKey(item.RELIGION_CODE))
                        {
                            dic.Add(item.RELIGION_CODE, item);
                        }
                    }
                }
            }
            catch (Exception ex)
            {
                Logging(Inventec.Common.Logging.LogUtil.TraceData(Inventec.Common.Logging.LogUtil.GetMemberName(() => search), search) + Inventec.Common.Logging.LogUtil.TraceData(Inventec.Common.Logging.LogUtil.GetMemberName(() => param), param), LogType.Error);
                LogSystem.Error(ex);
                dic.Clear();
            }
            return dic;
        }
    }
}
